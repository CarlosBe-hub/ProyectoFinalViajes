    private void jBbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBbuscarActionPerformed

        String Ttransporte = jTciudad.getText();

        modelo.setRowCount(0);

        if (!Ttransporte.isEmpty()) {
            List<Pasaje> pasaje = pd.buscarPasajePorTipoTransporte(Ttransporte);

            if (!pasaje.isEmpty()) {
                for (Pasaje p : pasaje) {
                    Object[] fila = new Object[5];
                    fila[0] = p.getId_pasaje();
                    fila[1] = p.getCiudadOrigen().getNombre();
                    fila[2] = p.getImporte();
                    fila[3] = p.getTipoTransporte();
                    fila[4] = p.isEstado();

                    modelo.addRow(fila);
                }
            }else{
                JOptionPane.showMessageDialog(null, "No se encontro ningun pasaje");
            }

            } else {
                JOptionPane.showMessageDialog(null, "Por favor ingrese el nombre de la ciudad!");
            }
    }//GEN-LAST:event_jBbuscarActionPerformed

    private void jBeliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBeliminarActionPerformed

    int filaSeleccionada = jTpasaje.getSelectedRow();
    if (filaSeleccionada != -1) {
        int idPasaje = (int) modelo.getValueAt(filaSeleccionada, 0); 
        int confirmacion = JOptionPane.showConfirmDialog(null, "Â¿Estas seguro de eliminar este pasaje?", "Confirme Por favor!", JOptionPane.YES_NO_OPTION);
        
        if (confirmacion == JOptionPane.YES_OPTION) {
            pd.eliminarPasaje(idPasaje); 
            modelo.removeRow(filaSeleccionada); 
            JOptionPane.showMessageDialog(null, "El Pasaje se elimino con exito.");
        }
    } else {
        JOptionPane.showMessageDialog(null, "Seleccione un pasaje para eliminar.");
    }

    }//GEN-LAST:event_jBeliminarActionPerformed


